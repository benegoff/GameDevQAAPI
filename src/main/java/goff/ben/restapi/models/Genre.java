package goff.ben.restapi.models;

public class Genre {
    private int id;
    private string name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public string getName() {
        return name;
    }

    public void setName(string name) {
        this.name = name;
    }
}
